package org.korecky.bluetooth.client.hc06.entity;

/**
 *
 * @author vkorecky
 */
public class Service {

    String id;
    String url;
    String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
